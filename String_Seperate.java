package string;

public class String_Seperate {

	public static void main(String[] args) {

		String s = "manish kumar tiwari";
		System.out.println(s.substring(0));
		System.out.println(s.substring(0, 6));
		System.out.println(s.substring(7, 12));
		//System.out.println(s.substring(13, 19));
		System.out.println(s.substring(13));// will give the value from given index till end
	}

}
